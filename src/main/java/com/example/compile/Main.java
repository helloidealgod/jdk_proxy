    public static String getToken() throws IOException {
        int read;
        char c;
        StringBuilder token = new StringBuilder("");
        int status = 0;
        int index;
        while ((read = pr.read()) != -1) {
            c = (char) read;
            index = getIndex(c);
            status = stateMap[status][index];
            if (0 == status) {
                token.append(c);
                return token.toString();
            } else if (-1 == status) {
                pr.unread(c);
                return token.toString();
            } else if (-2 == status) {
                System.out.println("error:" + token.toString() + " " + c);
                break;
            } else if (-3 == status) {
                status = 0;
            } else {
                token.append(c);
            }
        }
        return null;
    }

    public static void pushToken(String token) throws IOException {
        pr.unread(token.toCharArray());
    }

    /**
     * <translationUnit>:={<externalDeclaration>}<TK_EOF>
     *
     * @throws IOException
     */
    public static void translationUnit() throws IOException {
        String token;
        while ((token = getToken()) != null) {
//            System.out.println(token);
            externalDeclaration(token);
        }
    }

    /**
     * 解析外部声明
     * <externalDeclaration>:={<functionDefinition>}|{<declaration>}
     * 函数定义或变量声明
     *
     * @throws IOException
     */
    public static void externalDeclaration(String token) throws IOException {
        StringBuilder sb = new StringBuilder("");
        if (token.matches("#include")) {
            sb.append(token);
            sb.append(" ");
            token = getToken();
            if (token.matches("<")) {
                token = getToken();
                sb.append(token);
                token = getToken();
                if (token.matches(">")) {
                    System.out.println(sb.toString());
                } else {
                    System.out.println("error");
                }
            } else if (token.startsWith("\"")) {
                sb.append(token);
                System.out.println(sb.toString());
            } else {
                System.out.println("error");
            }
        } else if (token.matches("int|float|char|long|")) {
            //记录类型
            sb.append(token);
            sb.append(" ");
            token = getToken();
            while (true) {
                //是否是变量名
                if (token.matches("[_A-Za-z][_A-Za-z0-9]*")) {
                    sb.append(token);
                    token = getToken();
                    if (token.matches("\\(")) {
                        //函数参数列表
                        //函数 functionBody()
                        break;
                    } else if (token.matches("=")) {
                        //变量 =
                        sb.append(token);
                        //取数
                        token = getToken();
                        sb.append(token);
                        //取标点符号
                        token = getToken();
                        if (token.matches(";")) {
                            System.out.println(sb.toString());
                            break;
                        } else if (token.matches(",")) {
                            sb.append(token);
                            token = getToken();
                        }
                    } else if (token.matches("\\[")) {
                        do {
                            sb.append(token);
                            token = getToken();
                            sb.append(token);
                            token = getToken();
                            if ("]".equals(token)) {
                                sb.append(token);
                                token = getToken();
                                if (";".equals(token)) {
                                    System.out.println(sb.toString());
                                    break;
                                }
                            } else if (";".equals(token)) {
                                System.out.println(sb.toString());
                                break;
                            }
                        } while (true);
                        break;
                    } else if (token.matches(",")) {
                        sb.append(token);
                        token = getToken();
                    } else if (token.matches(";")) {
                        System.out.println(sb.toString());
                        break;
                    }
                } else if ("*".equals(token)) {
                    sb.append(token);
                    token = getToken();
                } else {
                    break;
                }
            }
        }
    }

